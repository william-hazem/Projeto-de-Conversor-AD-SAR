  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  /// cerca de 1 * 0,01 = 10 ms
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  TIM3->CCR1 = HSPeriod / 10;	/// timer do H/S
  TIM2->CCR2 = PWMPeriod;



  while (1)
  {
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);

	while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_7)) continue;

	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);


	count = 8;
	sar = 0x00;

#ifdef TESTE_PWM

	testPWM();
	while(1);
#else

	while(count > 0)
	{
		uint8_t temp = sar | (1 << count);
		float invertingInput = temp * Vref / 255;
		float dutyCicle = invertingInput / Vref; // Vref aqui refere-se ao maior nível de tensão do pwm
		TIM2->CCR2 = PWMPeriod * dutyCicle;
		HAL_Delay(5); /// pelo menos 5ms
		GPIO_PinState ret = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8);
		if(ret)
			sar = temp;

		ledLine(sar);
		count--;
	}
#endif


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
